function changeHeight() {
  document.getElementById("box").style.height="250px"
}

function makeBlue() {
    document.getElementById("box").style.backgroundColor="blue"
}

function makeDim() {
    document.getElementById("box").style.opacity=".4"
}

function tiny() {
    document.getElementById("box").style.height="30px"
    document.getElementById("box").style.width="30px"
}

function outline() {
    document.getElementById("box").style.outline=" thick solid black"
}

function Reset() {
    document.getElementById("box").style.height="150px"
    document.getElementById("box").style.width="150px"
    document.getElementById("box").style.backgroundColor="orange"
    document.getElementById("box").style.opacity="1.0"
    document.getElementById("box").style.outline="none"
}